'''
损失函数介绍：
    概述：
        损失函数，用来衡量 模型好坏（模型拟合情况）的
    分类：
        分类问题：
            多分类交叉熵损失：CrossEntropyLoss
            二分类交叉熵损失：BCELoss
        回归问题：
            MAE：
            MSE:
            Smooth L1

多分类交叉熵损失：CrossEntropyLoss
    设计思路：
        Loss = - ∑ylog(S(f(x)))
        x:          样本
        f(X):       加权求和，模型预测值
        S(f(x)):	处理后的概率，经过softmax处理
        Y : 		样本x属于某一个类别的 真实概率
    大白话解释：
        损失函数结果 = 正确类别概率的对数的最小化
    细节：
    	CrossEntropyLoss = softmax（） + 损失计算，后续如果用这个损失函数，则输出层就不用额外调用softmax（）激活函数了
'''

# 适用于多分类
import torch
import torch.nn as nn

def dm01():
	# 手动创建样本的真实y值
	# y_true = torch.tensor(data=[[0, 1, 0], [1, 0, 0]], dtype=torch.float32)
	y_true = torch.tensor(data=[1, 2])
	print('y_true->', y_true.dtype)
	print(y_true)
	# 手动创建样本的预测y值 -> 模型预测值f(X)
	y_pred = torch.tensor(data=[[2.5, -0.3, 1.0], [0.5, -1.2, 3.0]], requires_grad=True, dtype=torch.float32)
	# 创建多分类交叉熵损失对象
	# reduction:损失值计算的方式, 默认mean 平均损失值
	criterion = nn.CrossEntropyLoss()
	# 调用损失对象计算损失值
	# 预测y  真实y
	loss = criterion(y_pred, y_true)
	print('loss->', loss)


if __name__ == '__main__':
	dm01()


'''
y_true = torch.tensor(data=[[0, 1, 0], [1, 0, 0]], dtype=torch.float32)的计算逻辑
                                                 
● [[0, 1, 0], [1, 0, 0]] 是一个 one-hot 编码的张量，形状为 (2, 3)，表示       
  2个样本，3个分类。                                   
                                                                              
  逐行理解：                                                                  
                                                                              
  ┌───────┬───────────┬──────────────────────────┐                            
  │ 样本  │   向量    │           含义           │
  ├───────┼───────────┼──────────────────────────┤
  │ 样本1 │ [0, 1, 0] │ 第1个位置是1 → 属于类别1 │
  ├───────┼───────────┼──────────────────────────┤
  │ 样本2 │ [1, 0, 0] │ 第0个位置是1 → 属于类别0 │
  └───────┴───────────┴──────────────────────────┘

  每一行都是一个 one-hot向量：3个位置对应3个类别，真实类别的位置为1，其余为0。

  ---
  再看 y_pred 的形状也是 (2,3)，每一行是模型为每个类别打出的原始分数（logits）：

  y_pred = [[2.5, -0.3, 1.0],   #样本1：类别0得分2.5，类别1得分-0.3，类别2得分1.0
            [0.5, -1.2, 3.0]]   #样本2：类别0得分0.5，类别1得分-1.2，类别2得分3.0
  
- 样本1：真实类别是1（得分-0.3），但类别0得分最高（2.5）→ 预测错了，损失大
- 样本2：真实类别是0（得分0.5），但类别2得分最高（3.0）→ 预测错了，损失大

'''

'''
y_true = torch.tensor(data=[1, 2]的计算逻辑
 y_pred = [[2.5, -0.3, 1.0],   # 样本0通过神经网络加权求和之后对每类样本预测的得分
            [0.5, -1.2, 3.0]]   # 样本1通过神经网络加权求和之后对每类样本预测的得分                                 
  y_true = [1, 2]               # 样本0真实类别=1，样本1真实类别=2

  第1步：对 y_pred 每行做 Softmax，得到概率

  样本0 [2.5, -0.3, 1.0]：
  e^2.5=12.18,  e^-0.3=0.74,  e^1.0=2.72  →  sum=15.64
  softmax → [0.779, 0.047, 0.174]

  样本1 [0.5, -1.2, 3.0]：
  e^0.5=1.65,  e^-1.2=0.30,  e^3.0=20.09  →  sum=22.04
  softmax → [0.075, 0.014, 0.912]

  第2步：取真实类别对应的概率，算 -ln

  样本0 → 类别1的概率=0.047  →  -ln(0.047) = 3.049
  样本1 → 类别2的概率=0.912  →  -ln(0.912) = 0.092

  第3步：求均值（reduction='mean'）

  loss = (3.049 + 0.092) / 2 = 1.571


'''